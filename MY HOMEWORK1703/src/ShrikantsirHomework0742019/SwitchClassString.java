package ShrikantsirHomework0742019;
//Demonstrate using switch statements with string expression
public class SwitchClassString {
    public static void main(String[] args) {
        String dayoftheweek = "Tuesday";
        switch (dayoftheweek) {
            case "Monday":
                System.out.println("i like Monday");
                break;
            case "Tuesday":
                System.out.println("Tuesday is double delivery in grocery");
                break;
            case "wendesday":
                System.out.println("wed.i dont like");
                break;
            default:
                System.out.println("any days of the week");
                break;
        }
    }
}











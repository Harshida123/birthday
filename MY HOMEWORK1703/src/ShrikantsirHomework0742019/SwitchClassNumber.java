package ShrikantsirHomework0742019;

public class SwitchClassNumber {
    //Demonstrate using switch statements with number.
    public static void main(String[] args){
        int b =5;
        switch (b){
            case 1:
                System.out.println("the value of b is :"+1);
                break;
            case 2:
                System.out.println("the value of b is :" +2);
                break;
            case 3:
                System.out.println("the value of b is:" +3);
                break;
            case 5:
                System.out.println("the value of b is :"+5);
                break;
            default:
                System.out.println("any value for b");
        }
    }
}
